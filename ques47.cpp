/*Explain the concept of histogram problems and their applications in algorithm design.

A histogram is typically a bar chart where each bar has a certain height.

Ans :- In algorithm problems, the Histogram Problem usually refers to finding some property about these bars, for example:
The largest rectangle that can be formed inside the histogram.
The amount of water that can be trapped between bars after rain.
In most problems:
Each bar represents a value (like height).
The position of a bar (index) is important.

Example Problems:
Largest Rectangle in Histogram:
Find the area of the largest rectangle that fits entirely inside the bars.
Classic use of Stacks to optimize.
Trapping Rain Water:
Given an array of heights, calculate how much water can be trapped between them.
Can be solved using two pointers or prefix/suffix arrays.*/

